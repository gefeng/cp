void run_case() {
    std::string S;
    std::cin >> S;

    for(int i = 0; i < 16; i++) {
        if(i & 1) {
            if(S[i] == '1') {
                std::cout << "No" << '\n';
                return;
            }
        }
    }

    std::cout << "Yes" << '\n';
}

