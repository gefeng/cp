void run_case() {
    int S;
    std::cin >> S;

    int x = 1;
    int ans = 0;
    while(S > 0) {
        S -= x;
        ans += 1;
        x += 2;
    }
    
    std::cout << ans << '\n';
}

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(0);
    
    int T;
    std::cin >> T;
    while(T--) {
        run_case();
    }
}
