void run_case() {
    int N, W;
    std::cin >> N >> W;
    
    int64_t ans = static_cast<int64_t>(N / W) * (W - 1) + (N % W);
    
    std::cout << ans << '\n';
}

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(0);
    
    int T;
    std::cin >> T;
    while(T--) {
        run_case();
    }
}
