void run_case() {
    int N;
    std::cin >> N;

    if(N % 2 == 0) {
        std::cout << 0 << ' ' << 0 << ' ' << N / 2 << '\n';
        return;
    }

    std::cout << -1 << '\n';
}

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(0);
    
    int T;
    std::cin >> T;
    while(T--) {
        run_case();
    }
}
